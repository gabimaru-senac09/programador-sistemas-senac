numero_um = float(input("Digite um numero: "))
numero_dois = float(input("Digite outro numero: "))

# Adição
print(f"Soma de {numero_um} e {numero_dois} = ", numero_um + numero_dois)

# Subtração
print(f"Subtração de {numero_um} e {numero_dois} = ", numero_um - numero_dois)

# Multiplicação
print(f"Multiplicação de {numero_um} e {numero_dois} = ", numero_um * numero_dois)

# Divisão
print(f"Divisão de {numero_um} e {numero_dois} = ", numero_um / numero_dois)

# Restante
print(f"Restante da divisão {numero_um} e {numero_dois} = ", numero_um % numero_dois)

# Potência
print(f"Potência de {numero_um} e {numero_dois} = ", numero_um ** numero_dois)

